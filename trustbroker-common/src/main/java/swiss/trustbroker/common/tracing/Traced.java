package swiss.trustbroker.common.tracing;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Annotate injected classes on class or public method level to get tracing.
 * Only use on classes/methods implementing remote calls.
 */
@Target({METHOD, TYPE})
@Retention(RUNTIME)
public @interface Traced {

	 // @return the name of the span to create.
	String name() default "";

	// add information to parent span instead of creating a new one, if it exists.
	boolean join() default true;
}